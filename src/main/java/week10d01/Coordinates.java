package week10d01;

public class Coordinates {
    float xCoordinate;
    float yCoordinate;
    float zCoordinate;

    public Coordinates(float xCoordinate, float yCoordinate, float zCoordinate) {
        this.xCoordinate = xCoordinate;
        this.yCoordinate = yCoordinate;
        this.zCoordinate = zCoordinate;
    }

    public float getxCoordinate() {
        return xCoordinate;
    }

    public float getyCoordinate() {
        return yCoordinate;
    }

    public float getzCoordinate() {
        return zCoordinate;
    }
}
